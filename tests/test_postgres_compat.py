def test_basic_select(pg_conn):
    """基本 SELECT 查询"""
    result = pg_conn.execute("SELECT 1 AS val").fetchone()
    assert result["val"] == 1


def test_parameterized_query(pg_conn):
    """参数化查询（? 占位符自动转换为 %s）"""
    result = pg_conn.execute(
        "SELECT ? AS a, ? AS b",
        ("hello", 42),
    ).fetchone()
    assert result["a"] == "hello"
    assert result["b"] == 42


def test_row_factory_dict_access(pg_conn):
    """行结果支持字典式访问"""
    result = pg_conn.execute("SELECT 1 AS id, 'test' AS name").fetchone()
    assert result["id"] == 1
    assert result["name"] == "test"


