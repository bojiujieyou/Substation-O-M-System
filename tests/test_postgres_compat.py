def test_create_table_and_insert(pg_clean_db):
    """CREATE TABLE + INSERT + SELECT"""
    conn = pg_clean_db
    conn.execute("""
        CREATE TABLE _test_compat (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name TEXT NOT NULL,
            value INTEGER DEFAULT 0
        )
    """)
    conn.commit()

    conn.execute("INSERT INTO _test_compat (name, value) VALUES (?, ?)", ("alpha", 10))
    conn.execute("INSERT INTO _test_compat (name, value) VALUES (?, ?)", ("beta", 20))
    conn.commit()

    rows = conn.execute("SELECT name, value FROM _test_compat ORDER BY name").fetchall()
    assert len(rows) == 2
    assert rows[0]["name"] == "alpha"
    assert rows[1]["value"] == 20


def test_update_and_delete(pg_clean_db):
    """UPDATE / DELETE 操作"""
    conn = pg_clean_db
    conn.execute("""
        CREATE TABLE _test_compat (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name TEXT NOT NULL,
            status TEXT DEFAULT 'active'
        )
    """)
    conn.commit()

    conn.execute("INSERT INTO _test_compat (name) VALUES (?)", ("item1",))
    conn.execute("INSERT INTO _test_compat (name) VALUES (?)", ("item2",))
    conn.commit()

    conn.execute("UPDATE _test_compat SET status = ? WHERE name = ?", ("closed", "item1"))
    conn.commit()

    active = conn.execute("SELECT COUNT(*) AS cnt FROM _test_compat WHERE status = 'active'").fetchone()
    assert active["cnt"] == 1


